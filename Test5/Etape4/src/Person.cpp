#include "Person.h"
using namespace std;

/***************************************************************************************************************************/
////////////////////////////////////////////////////////// CONSTRUCTEURS ////////////////////////////////////////////////////
/***************************************************************************************************************************/
Person::Person()
{
	firstName = "No name";
	lastName = "No lastName";
}
Person::Person(const string l, const string f)
{
	setLastName(l);
	setFirstName(f);
}
Person::Person(const Person& p)
{
	setLastName(p.lastName);
	setFirstName(p.firstName);
}
/***************************************************************************************************************************/
////////////////////////////////////////////////////////// SETTERS //////////////////////////////////////////////////////////
/***************************************************************************************************************************/
void Person::setLastName(const string l)
{
	lastName = l;
}
void Person::setFirstName(const string f)
{
	firstName = f;
}
/***************************************************************************************************************************/
////////////////////////////////////////////////////////// GETTERS //////////////////////////////////////////////////////////
/***************************************************************************************************************************/
string Person::getLastName() const
{
	return lastName;
}
string Person::getFirstName() const
{
	return firstName;
}
/***************************************************************************************************************************/
////////////////////////////////////////////////////////// OPERATORS ////////////////////////////////////////////////////////
/***************************************************************************************************************************/
Person& Person::operator=(const Person& p)
{
	if (this == &p)
	{
		return *this;
	}
	this->firstName = p.firstName;
	this->lastName = p.lastName;
	return *this;
}
istream& operator>>(istream& s, Person& p)
{
	string f, l;
	s >> l;
	s.ignore();
	s >> f;
	s.ignore();
	p.firstName = f;
	p.lastName = l;
	return s;
}
ostream& operator<<(ostream& o, const Person& p)
{
	o << "Prenom: " << p.firstName << endl << "Nom: " << p.lastName << endl;
	return o;
}